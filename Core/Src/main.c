void set_servo_angle(uint8_t angle) {
  uint32_t pulse = 50 + ((250 - 50) * angle) / 180; // Convert angle to timer counts
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pulse);
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, pulse);
}
